"""
หัวข้อ: การสร้างอ็อบเจกต์ (Create Object)
อ้างอิงแนวคิดจาก: https://www.w3schools.com/python/python_oop.asp

อ็อบเจกต์ (object) คือตัวจริงที่ถูกสร้างขึ้นจากคลาส
เราสร้างอ็อบเจกต์โดยการ "เรียกใช้" คลาสเหมือนเรียกฟังก์ชัน
"""


class Robot:
    power = 100


# สร้างอ็อบเจกต์จากคลาส Robot
r1 = Robot()

# เข้าถึง property ผ่านตัวอ็อบเจกต์
print("อ็อบเจกต์ r1 มีพลังงาน:", r1.power)
